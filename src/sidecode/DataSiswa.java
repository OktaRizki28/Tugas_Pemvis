    protected void datatabel(){
        Object [] Baris = {"Nama Siswa", "Tanggal Lahir","Jenis Klamin","Alamat"};
        tabmode = new DefaultTableModel(null,Baris);
        tabelsiswa.setModel(tabmode);
        String sql = " SELECT * FROM siswa";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("nama");
                String b = hasil.getString("tgl");
                String c = hasil.getString("jk");
                String d = hasil.getString("alamat");
            
                String[] data={a,b,c,d,};
                tabmode.addRow(data);
       
            } 
            
        }catch (Exception ex) {
    JOptionPane.showMessageDialog(null, "Error retrieving data: " + ex.getMessage()); 
           
        }
    }


    

