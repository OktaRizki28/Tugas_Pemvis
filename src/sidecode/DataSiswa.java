    private void tabelsiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelsiswaMouseClicked
        // TODO add your handling code here:
        int bar = tabelsiswa.getSelectedRow();
    if (bar != -1) { 
    String a = tabmode.getValueAt(bar, 0).toString(); 
    String b = tabmode.getValueAt(bar, 1).toString(); 
    String c = tabmode.getValueAt(bar, 2).toString(); 
    String d = tabmode.getValueAt(bar, 3).toString(); 

 
    Tnama.setText(a);

    // Set the date in the date picker
    try {
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        Date parsedDate = format.parse(b); 
        Ttgl.setDate(parsedDate);
    } catch (ParseException e) {
        System.out.println("Error parsing date: " + e.getMessage());
        JOptionPane.showMessageDialog(null, "Error parsing date: " + e.getMessage());
    }
    Rjk2.setSelected(true);
    
    
    if (c.equals("laki-laki")) {
        Rjk1.setSelected(true);
        Rjk2.setSelected(false);
    } else {
        Rjk1.setSelected(false);
        Rjk2.setSelected(true);
    }

  
    Talm.setText(d);
} else {
    System.out.println("No row selected.");
    JOptionPane.showMessageDialog(null, "Please select a row to edit.");
}
        
    }//GEN-LAST:event_tabelsiswaMouseClicked

    
    
