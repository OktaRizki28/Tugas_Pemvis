    private void tabelsiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelsiswaMouseClicked
        int bar = tabelsiswa.getSelectedRow();
        b_hapus.setVisible(true);
        b_edit.setVisible(true);
        if (bar != -1) {
            String a = tabmode.getValueAt(bar, 0).toString();
            String b = tabmode.getValueAt(bar, 1).toString();
            String c = tabmode.getValueAt(bar, 2).toString();
            String d = tabmode.getValueAt(bar, 3).toString();
            String e = tabmode.getValueAt(bar, 4).toString();

            tid1.setText(a);
            tnama1.setText(b);
            // Set the date in the date picker
            try {
                SimpleDateFormat format = new SimpleDateFormat("dd-MM-yyyy");
                Date parsedDate = format.parse(c);
                ttgl1.setDate(parsedDate);
            } catch (ParseException ex) {
                System.out.println("Error parsing date: " + ex.getMessage());
                JOptionPane.showMessageDialog(null, "Error parsing date: " + ex.getMessage());
            }
            rjk3.setSelected(true);

            if (d.equals("laki-laki")) {
                rjk3.setSelected(true);
                rjk4.setSelected(false);
            } else {
                rjk3.setSelected(false);
                rjk4.setSelected(true);
            }

            talm1.setText(e);
        } else {
            System.out.println("No row selected.");
            JOptionPane.showMessageDialog(null, "Please select a row to edit.");
        }
    }//GEN-LAST:event_tabelsiswaMouseClicked


