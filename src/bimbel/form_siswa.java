    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
          Object[] Baris = { "ID siswa", "Nama Siswa", "Tanggal Lahir", "Jenis Kelamin", "Alamat" };
    tabmode = new DefaultTableModel(null, Baris);
    tabelsiswa.setModel(tabmode);

    // Dapatkan istilah pencarian dari kolom teks
    String searchTerm = tcari.getText();

    
    String sql = "SELECT * FROM tabel_siswa WHERE id_siswa LIKE ? OR nama_siswa LIKE ?";

    try {
        PreparedStatement stat = conn.prepareStatement(sql);
        String wildcardTerm = "%" + searchTerm + "%";
        stat.setString(1, wildcardTerm); // For id
        stat.setString(2, wildcardTerm); // For nama

        ResultSet hasil = stat.executeQuery();

        // Hapus hasil sebelumnya
        tabmode.setRowCount(0);

        //Isi tabel dengan hasil
        boolean hasResults = false;
        while (hasil.next()) {
            hasResults = true;
            String a = hasil.getString("id_siswa");
            String b = hasil.getString("nama_siswa");
            String c = hasil.getString("tgl");
            String d = hasil.getString("jk");
            String e = hasil.getString("alamat");

            String[] data = { a, b, c, d, e };
            tabmode.addRow(data);
        }

        if (!hasResults) {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan.");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_bcariActionPerformed

    private void btambah3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btambah3ActionPerformed
      try {
    // Memeriksa apakah ID tidak kosong
    if (tid1.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "ID siswa tidak boleh kosong.");
        return;
    }

    // Corrected SQL Syntax: Removed comma before WHERE
    String sql = "UPDATE tabel_siswa SET nama_siswa=?, tgl=?, jk=?, alamat=? WHERE id_siswa=?";
    PreparedStatement stat = conn.prepareStatement(sql);
    
    // Mengatur parameter
    stat.setString(1, tnama1.getText());
    
    String jkel = rjk3.isSelected() ? "Laki-Laki" : "Perempuan";
    stat.setString(3, jkel);
    
    stat.setString(4, talm1.getText());
    
    if (ttgl1.getDate() != null) {
        Date selectedDate = (Date) ttgl1.getDate();
        SimpleDateFormat format = new SimpleDateFormat("dd-MM-yyyy"); // Change format as needed for DB compatibility
        String formattedDate = format.format(selectedDate);
        stat.setString(2, formattedDate);
    } else {
        // Handle case if date is not selected
        JOptionPane.showMessageDialog(null, "Tanggal tidak boleh kosong.");
        return; // Exit if date is required
    }

    stat.setString(5, tid1.getText());
    
    // Eksekusi update
    int rowsUpdated = stat.executeUpdate();
    if (rowsUpdated > 0) {
        JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
        kosong(); // Panggil metode untuk mengosongkan form
        tid1.requestFocus(); // Fokus kembali ke field ID
        datatabel(); // Memperbarui tampilan tabel
    } else {
        JOptionPane.showMessageDialog(null, "Data tidak ditemukan untuk diubah.");
    }
} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "Data Gagal Diubah: " + e.getMessage());
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
} 
    }//GEN-LAST:event_btambah3ActionPerformed

